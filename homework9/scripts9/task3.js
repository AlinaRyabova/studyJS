// 3. Користувач вводить кількість елементів. Створити масив, у якому перші 5 елементів заповнені 1-цями, а інші до кінця масиву заповнені 7-ками.

if (confirm("Почати виведення?")) {
  let numElements = parseInt(prompt("Введіть кількість елементів масиву:"));
  let array = new Array(numElements).fill(1, 0, 5).fill(7, 5);
  document.write(
    "Масив з " + numElements + " елементів, перші 5 - 1, інші - 7: " + array
  );
}
