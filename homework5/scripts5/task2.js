if (confirm("Почати виведення місяців?")) {
  for (let month = 3; month <= 8; month++)
    if (month >= 6) {
      alert(`Місяць номер: ${month} -це літо`);
    } else {
      alert(`Місяць номер: ${month} -це весна`);
    }
}
