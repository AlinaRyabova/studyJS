// 7.Дано масив цін. Змінити цей масив так, що на ціни товарів, які більші за 1000 грн. дати 30% знижки.

if (confirm("Почати виведення?")) {
  let prices = [500, 1500, 2500, 750, 3000, 1250, 400];
  for (let i = 0; i < prices.length; i++) {
    if (prices[i] > 1000) {
      prices[i] = prices[i] * 0.7; // Застосовуємо знижку 30%
    }
  }
  document.write("Масив цін після застосування знижки: " + prices);
}
