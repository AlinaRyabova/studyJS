// Задача 4. Дано масив хлопців і дівчат вивести всі можливі комбінації для танців з хлопців і дівчат.

const boys: string[] = ["Іван", "Петро", "Сергій"];
const girls: string[] = ["Оля", "Катя", "Маша"];

/**
 * Виводить всі можливі комбінації для танців з хлопців і дівчат.
 */

for (let boy of boys) {
  for (let girl of girls) {
    document.writeln(`${boy} пара з ${girl}<br>`);
  }
}
