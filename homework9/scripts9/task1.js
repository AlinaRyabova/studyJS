// 1. Користувач вводить кількість елементів. Створити масив, що складається з вказаної кількості елементів заповнених нулями.
if (confirm("Почати виведення?")) {
  let numElements = parseInt(prompt("Введіть кількість елементів масиву:"));
  let array = new Array(numElements).fill(0);
  document.write(
    "Масив з " + numElements + " елементів, заповнений нулями: " + array
  );
}
